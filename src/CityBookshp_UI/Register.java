    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
          boolean vaild=true;
        
        if(txtUserId.getText().toString().isEmpty() ||
           txtPassword.getText().toString().isEmpty() ||
          txtConPassword.getText().toString().isEmpty() ||
          cmbUserType.getSelectedItem().toString().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "File shouldn't be Blank", "Register Form", JOptionPane.ERROR_MESSAGE);
            vaild =false;//
       }
        if( txtPassword.getText().toString().length()<5){
              JOptionPane.showMessageDialog(rootPane, "Password must have more than 05 characters", "Register Form", JOptionPane.ERROR_MESSAGE);
            vaild =false;//
            
        }
       if(!txtPassword.getText().toString().equals( txtConPassword.getText().toString())){
           JOptionPane.showMessageDialog(rootPane, "Password and ConfirmPassword shoud match", "Register Form", JOptionPane.ERROR_MESSAGE);
           vaild =false;
       }
       if(vaild){
       obj.setUserId(txtUserId.getText().trim());
         obj.setPassword(txtPassword.getText());
             obj. setUserType(cmbUserType.getSelectedItem().toString());
             int noRows=obj.addUser();
              if(noRows>0){
                  JOptionPane.showMessageDialog(rootPane,"Data Inserted");
              }
              else{
                  JOptionPane.showMessageDialog(rootPane, "Data is not Vaild", "Register Form", JOptionPane.ERROR_MESSAGE);
          }
        }
        
    }//GEN-LAST:event_btnRegisterActionPerformed

